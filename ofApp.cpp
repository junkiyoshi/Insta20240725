#include "ofApp.h"	

//--------------------------------------------------------------
void ofApp::setup() {

	ofSetFrameRate(25);
	ofSetWindowTitle("openFrameworks");

	ofBackground(39);
	ofEnableDepthTest();

	this->frame.setMode(ofPrimitiveMode::OF_PRIMITIVE_LINES);
	this->noise_step = ofRandom(1000);
}

//--------------------------------------------------------------
void ofApp::update() {

	auto param = ofGetFrameNum() % 50;
	if (param < 20) { this->noise_step += 0; }
	else if (param < 25) { this->noise_step += ofMap(param, 20, 25, 0, 0.08);	}
	else if (param < 45) { this->noise_step += 0.08; }
	else { this->noise_step += ofMap(param, 45, 50, 0.08, 0); }

	this->face.clear();
	this->frame.clear();

	auto size = 50;
	for (int x = -1000; x <= 1000; x += size) {

		for (int y = -1000; y <= 1000; y += size) {

			auto noise_value = ofNoise(x * 0.001, y * 0.001 + this->noise_step);
			noise_value = noise_value < 0.5 ? 0 : 1;
			auto height = ofMap(noise_value, 0, 1, 50, 500);

			for (int z = 0; z <= height; z += size) {

				ofColor fill_color(ofMap(z, 0, 500, 239, 0));
				this->setBoxToMesh(this->face, this->frame, glm::vec3(x, y, z), size, fill_color, ofColor(255));
			}
		}

	}
}

